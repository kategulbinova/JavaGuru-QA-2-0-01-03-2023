package svetlanakussi.homework;

public class ArraysHomework6 {
    static void printArray(int value) {
        int element = value + 5;
        System.out.println("Increase element by 5 is: " + element);
    }

    public static void main(String[] args) {
        printArray(3);
        printArray(6);
        printArray(9);
        printArray(13);
        printArray(17);
        printArray(-8);
        printArray(-11);
        printArray(-21);
        printArray(-111);

    }
}

